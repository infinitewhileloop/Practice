let magic = {
    replace: function(str, oldChar, newChar) {
        return str.replace(oldChar, newChar);
    },
    length: function(str) {
        return str.length;
    },
    trim: function(str) {
        return str.trim();
    },
    slice: function(arr, start, end) {
        return arr.slice(start, end);
    }
}
